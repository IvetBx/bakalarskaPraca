package com.balintova.repositoryOfRecipe.models;

public class XSDDuration {
    public Integer months = 0;
    public Integer hours = 0;
    public Integer minutes = 0;
    public Integer years = 0;
    public Integer days = 0;
    public Integer seconds = 0;
    public Integer fullSeconds = 0;
    public Integer timePart = 0;
    public Integer bigSeconds = 0;

    public Integer getMonths() {
        return months;
    }

    public void setMonths(Integer months) {
        this.months = months;
    }

    public Integer getHours() {
        return hours;
    }

    public void setHours(Integer hours) {
        this.hours = hours;
    }

    public Integer getMinutes() {
        return minutes;
    }

    public void setMinutes(Integer minutes) {
        this.minutes = minutes;
    }

    public Integer getYears() {
        return years;
    }

    public void setYears(Integer years) {
        this.years = years;
    }

    public Integer getDays() {
        return days;
    }

    public void setDays(Integer days) {
        this.days = days;
    }

    public Integer getSeconds() {
        return seconds;
    }

    public void setSeconds(Integer seconds) {
        this.seconds = seconds;
    }

    public Integer getFullSeconds() {
        return fullSeconds;
    }

    public void setFullSeconds(Integer fullSeconds) {
        this.fullSeconds = fullSeconds;
    }

    public Integer getTimePart() {
        return timePart;
    }

    public void setTimePart(Integer timePart) {
        this.timePart = timePart;
    }

    public Integer getBigSeconds() {
        return bigSeconds;
    }

    public void setBigSeconds(Integer bigSeconds) {
        this.bigSeconds = bigSeconds;
    }
}
